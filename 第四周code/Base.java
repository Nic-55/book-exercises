public class Base {
	public static void main(String[] args) {
		//进制演示
		int n1 = 0b1010; //0b开头,二进制
		int n2 = 1010; //十进制
		int n3 = 01010; //0开头,八进制
		int n4 = 0x10101; //0x开头,十六进 

		System.out.println("n1=" + n1);
		System.out.println("n2=" + n2);
		System.out.println("n3=" + n3);
		System.out.println("n4=" + n4);

		/*n1=10
          n2=1010
          n3=520
          n4=65793 */

	}
}